package edu.umkc.Bean;

public class SolrBean {

	private String key;
	private String caption;
	private String xml;
	private String nounLst;
	private String verbLst;
	
	public String getKey() {
		return key;
	}
	
	public void setKey(String key) {
		this.key = key;
	}
	
	public String getCaption() {
		return caption;
	}
	
	public void setCaption(String caption) {
		this.caption = caption;
	}
	
	public String getXml() {
		return xml;
	}
	
	public void setXml(String xml) {
		this.xml = xml;
	}
	
	public String getNounLst() {
		return nounLst;
	}
	
	public void setNounLst(String nounLst) {
		this.nounLst = nounLst;
	}
	
	public String getVerbLst() {
		return verbLst;
	}
	
	public void setVerbLst(String verbLst) {
		this.verbLst = verbLst;
	}
	
	
}
